//! File system implementation.  Five layers:
//!   + Blocks: allocator for raw disk blocks.
//!   + Log: crash recovery for multi-step updates.
//!   + Files: inode allocator, reading, writing, metadata.
//!   + Directories: inode with special contents (list of other inodes!)
//!   + Names: paths like /usr/rtm/xv6/fs.c for convenient naming.
//!
//! This file contains the low-level file system manipulation
//! routines.  The (higher-level) system call implementations
//! are in sysfile.c.

/// On-disk file system format used for both kernel and user programs are also included here.
use crate::{
    bio::brelease,
    buf::Buf,
    file::{Inode, InodeGuard, InodeInner},
    log::log_write,
    param::{NINODE, ROOTDEV},
    proc::{either_copyin, either_copyout},
    sleeplock::SleeplockWIP,
    spinlock::Spinlock,
    stat::{Stat, T_DIR, T_NONE},
};
use core::{mem, ops::DerefMut, ptr};

mod path;
pub use path::{FileName, Path};

/// Disk layout:
/// [ boot block | super block | log | inode blocks |
///                                          free bit map | data blocks]
///
/// mkfs computes the super block and builds an initial file system. The
/// super block describes the disk layout:
#[derive(Copy, Clone)]
pub struct Superblock {
    /// Must be FSMAGIC
    magic: u32,

    /// Size of file system image (blocks)
    size: u32,

    /// Number of data blocks
    nblocks: u32,

    /// Number of inodes
    ninodes: u32,

    /// Number of log blocks
    pub nlog: u32,

    /// Block number of first log block
    pub logstart: u32,

    /// Block number of first inode block
    inodestart: u32,

    /// Block number of first free map block
    bmapstart: u32,
}

/// dirent size
pub const DIRENT_SIZE: usize = mem::size_of::<Dirent>();

#[derive(Default)]
pub struct Dirent {
    pub inum: u16,
    name: [u8; DIRSIZ],
}

impl Dirent {
    /// Fill in name. If name is shorter than DIRSIZ, NUL character is appended as
    /// terminator.
    ///
    /// `name` must contains no NUL characters, but this is not a safety invariant.
    fn set_name(&mut self, name: &FileName) {
        let name = name.as_bytes();
        if name.len() == DIRSIZ {
            self.name.copy_from_slice(&name);
        } else {
            self.name[..name.len()].copy_from_slice(&name);
            self.name[name.len()] = 0;
        }
    }

    /// Returns slice which exactly contains `name`.
    ///
    /// It contains no NUL characters.
    fn get_name(&self) -> &FileName {
        let len = self.name.iter().position(|ch| *ch == 0).unwrap_or(DIRSIZ);
        unsafe { FileName::from_bytes(&self.name[..len]) }
    }

    // TODO: Use iterator
    fn read_entry(&mut self, ip: &mut InodeGuard<'_>, off: u32, panic_msg: &'static str) {
        unsafe {
            let bytes_read = ip.read(0, self as *mut Dirent as usize, off, DIRENT_SIZE as u32);
            assert_eq!(bytes_read, Ok(DIRENT_SIZE), "{}", panic_msg)
        }
    }
}

/// On-disk inode structure
/// Both the kernel and user programs use this header file.
// It needs repr(C) because it's struct for in-disk representation
// which should follow C(=machine) representation
// https://github.com/kaist-cp/rv6/issues/52
#[repr(C)]
struct Dinode {
    /// File type
    typ: i16,

    /// Major device number (T_DEVICE only)
    major: i16,

    /// Minor device number (T_DEVICE only)
    minor: i16,

    /// Number of links to inode in file system
    nlink: i16,

    /// Size of file (bytes)
    size: u32,

    /// Data block addresses
    addrs: [u32; 13],
}

/// Inodes.
///
/// An inode describes a single unnamed file.
/// The inode disk structure holds metadata: the file's type,
/// its size, the number of links referring to it, and the
/// list of blocks holding the file's content.
///
/// The inodes are laid out sequentially on disk at
/// SB.startinode. Each inode has a number, indicating its
/// position on the disk.
///
/// The kernel keeps a cache of in-use inodes in memory
/// to provide a place for synchronizing access
/// to inodes used by multiple processes. The cached
/// inodes include book-keeping information that is
/// not stored on disk: ip->ref and ip->valid.
///
/// An inode and its in-memory representation go through a
/// sequence of states before they can be used by the
/// rest of the file system code.
///
/// * Allocation: an inode is allocated if its type (on disk)
///   is non-zero. Inode::alloc() allocates, and Inode::put() frees if
///   the reference and link counts have fallen to zero.
///
/// * Referencing in cache: an entry in the inode cache
///   is free if ip->ref is zero. Otherwise ip->ref tracks
///   the number of in-memory pointers to the entry (open
///   files and current directories). iget() finds or
///   creates a cache entry and increments its ref; Inode::put()
///   decrements ref.
///
/// * Valid: the information (type, size, &c) in an inode
///   cache entry is only correct when ip->valid is 1.
///   Inode::lock() reads the inode from
///   the disk and sets ip->valid, while Inode::put() clears
///   ip->valid if ip->ref has fallen to zero.
///
/// * Locked: file system code may only examine and modify
///   the information in an inode and its content if it
///   has first locked the inode.
///
/// Thus a typical sequence is:
///   ip = iget(dev, inum)
///   (*ip).lock()
///   ... examine and modify ip->xxx ...
///   (*ip).unlock()
///   (*ip).put()
///
/// Inode::lock() is separate from iget() so that system calls can
/// get a long-term reference to an inode (as for an open file)
/// and only lock it for short periods (e.g., in read()).
/// The separation also helps avoid deadlock and races during
/// pathname lookup. iget() increments ip->ref so that the inode
/// stays cached and pointers to it remain valid.
///
/// Many internal file system functions expect the caller to
/// have locked the inodes involved; this lets callers create
/// multi-step atomic operations.
///
/// The ICACHE.lock spin-lock protects the allocation of icache
/// entries. Since ip->ref indicates whether an entry is free,
/// and ip->dev and ip->inum indicate which i-node an entry
/// holds, one must hold ICACHE.lock while using any of those fields.
///
/// An ip->lock sleep-lock protects all ip-> fields other than ref,
/// dev, and inum.  One must hold ip->lock in order to
/// read or write that inode's ip->valid, ip->size, ip->type, &c.

static mut ICACHE: Spinlock<[Inode; NINODE]> = Spinlock::new("ICACHE", [Inode::zeroed(); NINODE]);

//TODO(@kimjungwow) : move inode-related methods to another file
impl InodeGuard<'_> {
    /// Common idiom: unlock, then put.
    pub unsafe fn unlockput(self) {
        let ptr = self.ptr;
        drop(self);
        ptr.put();
    }

    /// Copy stat information from inode.
    /// Caller must hold ip->lock.
    pub unsafe fn stat(&self) -> Stat {
        Stat {
            dev: self.ptr.dev as i32,
            ino: self.ptr.inum,
            typ: self.typ,
            nlink: self.nlink,
            size: self.size as usize,
        }
    }

    // Directories
    /// Write a new directory entry (name, inum) into the directory dp.
    pub unsafe fn dirlink(&mut self, name: &FileName, inum: u32) -> Result<(), ()> {
        let mut de: Dirent = Default::default();

        // Check that name is not present.
        if let Ok((ip, _)) = self.dirlookup(name) {
            (*ip).put();
            return Err(());
        };

        // Look for an empty Dirent.
        let mut off: u32 = 0;
        while off < self.size {
            de.read_entry(self, off, "dirlink read");
            if de.inum == 0 {
                break;
            }
            off = (off as usize).wrapping_add(DIRENT_SIZE) as u32
        }
        de.inum = inum as u16;
        de.set_name(name);
        let bytes_write = self.write(0, &mut de as *mut Dirent as usize, off, DIRENT_SIZE as u32);
        assert_eq!(bytes_write, Ok(DIRENT_SIZE), "dirlink");
        Ok(())
    }

    /// Copy a modified in-memory inode to disk.
    /// Must be called after every change to an ip->xxx field
    /// that lives on disk, since i-node cache is write-through.
    /// Caller must hold self->lock.
    pub unsafe fn update(&self) {
        let bp: *mut Buf = Buf::read(self.ptr.dev, SB.iblock(self.ptr.inum));
        let mut dip: *mut Dinode = ((*bp).inner.data.as_mut_ptr() as *mut Dinode)
            .add((self.ptr.inum as usize).wrapping_rem(IPB));
        (*dip).typ = self.typ;
        (*dip).major = self.major as i16;
        (*dip).minor = self.minor as i16;
        (*dip).nlink = self.nlink;
        (*dip).size = self.size;
        (*dip).addrs.copy_from_slice(&self.addrs);
        log_write(bp);
        brelease(&mut *bp);
    }

    /// Truncate inode (discard contents).
    /// Only called when the inode has no links
    /// to it (no directory entries referring to it)
    /// and has no in-memory reference to it (is
    /// not an open file or current directory).
    unsafe fn itrunc(&mut self) {
        for i in 0..NDIRECT {
            if self.addrs[i] != 0 {
                bfree(self.ptr.dev as i32, self.addrs[i]);
                self.addrs[i] = 0
            }
        }
        if self.addrs[NDIRECT] != 0 {
            let bp = Buf::read(self.ptr.dev, self.addrs[NDIRECT]);
            let a = (*bp).inner.data.as_mut_ptr() as *mut u32;
            for j in 0..NINDIRECT {
                if *a.add(j) != 0 {
                    bfree(self.ptr.dev as i32, *a.add(j));
                }
            }
            brelease(&mut *bp);
            bfree(self.ptr.dev as i32, self.addrs[NDIRECT]);
            self.addrs[NDIRECT] = 0
        }
        self.size = 0;
        self.update();
    }

    /// Read data from inode.
    /// Caller must hold self->lock.
    /// If user_dst==1, then dst is a user virtual address;
    /// otherwise, dst is a kernel address.
    pub unsafe fn read(
        &mut self,
        user_dst: i32,
        mut dst: usize,
        mut off: u32,
        mut n: u32,
    ) -> Result<usize, ()> {
        if off > self.size || off.wrapping_add(n) < off {
            return Err(());
        }
        if off.wrapping_add(n) > self.size {
            n = self.size.wrapping_sub(off)
        }
        let mut tot: u32 = 0;
        while tot < n {
            let bp = Buf::read(self.ptr.dev, self.bmap((off as usize).wrapping_div(BSIZE)));
            let m = core::cmp::min(
                n.wrapping_sub(tot),
                (BSIZE as u32).wrapping_sub(off.wrapping_rem(BSIZE as u32)),
            );
            if either_copyout(
                user_dst,
                dst,
                (*bp)
                    .inner
                    .data
                    .as_mut_ptr()
                    .offset(off.wrapping_rem(BSIZE as u32) as isize),
                m as usize,
            )
            .is_err()
            {
                brelease(&mut *bp);
                break;
            } else {
                brelease(&mut *bp);
                tot = tot.wrapping_add(m);
                off = off.wrapping_add(m);
                dst = dst.wrapping_add(m as usize)
            }
        }
        Ok(n as usize)
    }

    /// Write data to inode.
    /// Caller must hold self->lock.
    /// If user_src==1, then src is a user virtual address;
    /// otherwise, src is a kernel address.
    pub unsafe fn write(
        &mut self,
        user_src: i32,
        mut src: usize,
        mut off: u32,
        n: u32,
    ) -> Result<usize, ()> {
        if off > self.size || off.wrapping_add(n) < off {
            return Err(());
        }
        if off.wrapping_add(n) as usize > MAXFILE.wrapping_mul(BSIZE) {
            return Err(());
        }
        let mut tot: u32 = 0;
        while tot < n {
            let bp = Buf::read(self.ptr.dev, self.bmap((off as usize).wrapping_div(BSIZE)));
            let m = core::cmp::min(
                n.wrapping_sub(tot),
                (BSIZE as u32).wrapping_sub(off.wrapping_rem(BSIZE as u32)),
            );
            if either_copyin(
                (*bp)
                    .inner
                    .data
                    .as_mut_ptr()
                    .offset(off.wrapping_rem(BSIZE as u32) as isize),
                user_src,
                src,
                m as usize,
            )
            .is_err()
            {
                brelease(&mut *bp);
                break;
            } else {
                log_write(bp);
                brelease(&mut *bp);
                tot = tot.wrapping_add(m);
                off = off.wrapping_add(m);
                src = src.wrapping_add(m as usize)
            }
        }
        if n > 0 {
            if off > self.size {
                self.size = off
            }
            // write the i-node back to disk even if the size didn't change
            // because the loop above might have called bmap() and added a new
            // block to self->addrs[].
            self.update();
        }
        Ok(n as usize)
    }

    /// Look for a directory entry in a directory.
    /// If found, return the entry and byte offset of entry.
    pub unsafe fn dirlookup(&mut self, name: &FileName) -> Result<(*mut Inode, u32), ()> {
        let mut de: Dirent = Default::default();
        assert_eq!(self.typ, T_DIR, "dirlookup not DIR");
        for off in (0..self.size).step_by(DIRENT_SIZE) {
            de.read_entry(self, off, "dirlookup read");
            if de.inum != 0 && name == de.get_name() {
                // entry matches path element
                return Ok((iget(self.ptr.dev, de.inum as u32), off));
            }
        }
        Err(())
    }

    /// Inode content
    ///
    /// The content (data) associated with each inode is stored
    /// in blocks on the disk. The first NDIRECT block numbers
    /// are listed in self->addrs[].  The next NINDIRECT blocks are
    /// listed in block self->addrs[NDIRECT].
    /// Return the disk block address of the nth block in inode self.
    /// If there is no such block, bmap allocates one.
    unsafe fn bmap(&mut self, mut bn: usize) -> u32 {
        let mut addr: u32;
        if bn < NDIRECT {
            addr = self.addrs[bn];
            if addr == 0 {
                addr = balloc(self.ptr.dev);
                self.addrs[bn] = addr
            }
            return addr;
        }
        bn = (bn).wrapping_sub(NDIRECT);

        assert!(bn < NINDIRECT, "bmap: out of range");
        // Load indirect block, allocating if necessary.
        addr = self.addrs[NDIRECT];
        if addr == 0 {
            addr = balloc(self.ptr.dev);
            self.addrs[NDIRECT] = addr
        }
        let bp: *mut Buf = Buf::read(self.ptr.dev, addr);
        let a: *mut u32 = (*bp).inner.data.as_mut_ptr() as *mut u32;
        addr = *a.add(bn);
        if addr == 0 {
            addr = balloc(self.ptr.dev);
            *a.add(bn) = addr;
            log_write(bp);
        }
        brelease(&mut *bp);
        addr
    }
}

impl Inode {
    /// Increment reference count for ip.
    /// Returns ip to enable ip = idup(ip1) idiom.
    pub unsafe fn idup(&mut self) -> *mut Self {
        let _inode = ICACHE.lock();
        self.ref_0 += 1;
        self
    }

    /// Lock the given inode.
    /// Reads the inode from disk if necessary.
    pub unsafe fn lock(&self) -> InodeGuard<'_> {
        assert!(self.ref_0 >= 1, "Inode::lock");
        let mut guard = self.inner.lock();
        if !guard.valid {
            let bp: *mut Buf = Buf::read(self.dev, SB.iblock(self.inum));
            let dip: *mut Dinode = ((*bp).inner.data.as_mut_ptr() as *mut Dinode)
                .add((self.inum as usize).wrapping_rem(IPB));
            guard.typ = (*dip).typ;
            guard.major = (*dip).major as u16;
            guard.minor = (*dip).minor as u16;
            guard.nlink = (*dip).nlink;
            guard.size = (*dip).size;
            guard.addrs.copy_from_slice(&(*dip).addrs);
            brelease(&mut *bp);
            guard.valid = true;
            assert_ne!(guard.typ, T_NONE, "Inode::lock: no type");
        };
        InodeGuard::new(guard, self)
    }

    /// Drop a reference to an in-memory inode.
    /// If that was the last reference, the inode cache entry can
    /// be recycled.
    /// If that was the last reference and the inode has no links
    /// to it, free the inode (and its content) on disk.
    /// All calls to Inode::put() must be inside a transaction in
    /// case it has to free the inode.
    #[allow(clippy::cast_ref_to_mut)]
    pub unsafe fn put(&self) {
        let mut inode = ICACHE.lock();

        if self.ref_0 == 1
            && self.inner.get_mut_unchecked().valid
            && self.inner.get_mut_unchecked().nlink == 0
        {
            // inode has no links and no other references: truncate and free.

            // self->ref == 1 means no other process can have self locked,
            // so this acquiresleep() won't block (or deadlock).
            let mut ip = self.lock();

            drop(inode);

            ip.itrunc();
            ip.typ = 0;
            ip.update();
            ip.valid = false;

            drop(ip);

            inode = ICACHE.lock();
        }
        //TODO : Use better code
        *(&self.ref_0 as *const _ as *mut i32) -= 1;
        drop(inode);
    }

    /// Allocate an inode on device dev.
    /// Mark it as allocated by  giving it type type.
    /// Returns an unlocked but allocated and referenced inode.
    pub unsafe fn alloc(dev: u32, typ: i16) -> *mut Inode {
        for inum in 1..SB.ninodes {
            let bp = Buf::read(dev, SB.iblock(inum));
            let dip = ((*bp).inner.data.as_mut_ptr() as *mut Dinode)
                .add((inum as usize).wrapping_rem(IPB));

            // a free inode
            if (*dip).typ == 0 {
                ptr::write_bytes(dip, 0, 1);
                (*dip).typ = typ;

                // mark it allocated on the disk
                log_write(bp);
                brelease(&mut *bp);
                return iget(dev, inum);
            }
            brelease(&mut *bp);
        }
        panic!("Inode::alloc: no inodes");
    }

    pub const fn zeroed() -> Self {
        // TODO: transient measure
        Self {
            dev: 0,
            inum: 0,
            ref_0: 0,
            inner: SleeplockWIP::new(
                "inode",
                InodeInner {
                    valid: false,
                    typ: 0,
                    major: 0,
                    minor: 0,
                    nlink: 0,
                    size: 0,
                    addrs: [0; 13],
                },
            ),
        }
    }
}

/// root i-number
const ROOTINO: u32 = 1;

/// block size
pub const BSIZE: usize = 1024;
const FSMAGIC: u32 = 0x10203040;
const NDIRECT: usize = 12;

const NINDIRECT: usize = BSIZE.wrapping_div(mem::size_of::<u32>());
const MAXFILE: usize = NDIRECT.wrapping_add(NINDIRECT);

/// Inodes per block.
const IPB: usize = BSIZE.wrapping_div(mem::size_of::<Dinode>());

impl Superblock {
    /// Block containing inode i
    const fn iblock(self, i: u32) -> u32 {
        i.wrapping_div(IPB as u32).wrapping_add(self.inodestart)
    }

    /// Block of free map containing bit for block b
    const fn bblock(self, b: u32) -> u32 {
        b.wrapping_div(BPB).wrapping_add(self.bmapstart)
    }

    /// Read the super block.
    unsafe fn read(&mut self, dev: i32) {
        let bp: *mut Buf = Buf::read(dev as u32, 1);
        ptr::copy(
            (*bp).inner.data.as_mut_ptr() as *const u8,
            self as *mut Superblock as *mut u8,
            mem::size_of::<Superblock>(),
        );
        brelease(&mut *bp);
    }

    // TODO: transient measure
    pub const fn zeroed() -> Self {
        Self {
            magic: 0,
            size: 0,
            nblocks: 0,
            ninodes: 0,
            nlog: 0,
            logstart: 0,
            inodestart: 0,
            bmapstart: 0,
        }
    }
}

/// Bitmap bits per block
const BPB: u32 = BSIZE.wrapping_mul(8) as u32;

/// Directory is a file containing a sequence of Dirent structures.
pub const DIRSIZ: usize = 14;

/// there should be one superblock per disk device, but we run with
/// only one device
static mut SB: Superblock = Superblock::zeroed();

/// Init fs
pub unsafe fn fsinit(dev: i32) {
    SB.read(dev);
    assert_eq!(SB.magic, FSMAGIC, "invalid file system");
    SB.initlog(dev);
}

/// Zero a block.
unsafe fn bzero(dev: i32, bno: i32) {
    let bp: *mut Buf = Buf::read(dev as u32, bno as u32);
    ptr::write_bytes((*bp).inner.data.as_mut_ptr(), 0, BSIZE);
    log_write(bp);
    brelease(&mut *bp);
}

/// Blocks.
/// Allocate a zeroed disk block.
unsafe fn balloc(dev: u32) -> u32 {
    let mut b: u32 = 0;
    let mut bi: u32 = 0;
    while b < SB.size {
        let mut bp: *mut Buf = Buf::read(dev, SB.bblock(b));
        while bi < BPB && (b + bi) < SB.size {
            let m = (1) << (bi % 8);
            if (*bp).inner.data[(bi / 8) as usize] as i32 & m == 0 {
                // Is block free?
                (*bp).inner.data[(bi / 8) as usize] =
                    ((*bp).inner.data[(bi / 8) as usize] as i32 | m) as u8; // Mark block in use.
                log_write(bp);
                brelease(&mut *bp);
                bzero(dev as i32, (b + bi) as i32);
                return b + bi;
            }
            bi += 1
        }
        brelease(&mut *bp);
        b += BPB
    }
    panic!("balloc: out of blocks");
}

/// Free a disk block.
unsafe fn bfree(dev: i32, b: u32) {
    let mut bp: *mut Buf = Buf::read(dev as u32, SB.bblock(b));
    let bi: i32 = b.wrapping_rem(BPB) as i32;
    let m: i32 = (1) << (bi % 8);
    assert_ne!(
        (*bp).inner.data[(bi / 8) as usize] as i32 & m,
        0,
        "freeing free block"
    );
    (*bp).inner.data[(bi / 8) as usize] = ((*bp).inner.data[(bi / 8) as usize] as i32 & !m) as u8;
    log_write(bp);
    brelease(&mut *bp);
}

/// Find the inode with number inum on device dev
/// and return the in-memory copy. Does not lock
/// the inode and does not read it from disk.
unsafe fn iget(dev: u32, inum: u32) -> *mut Inode {
    let mut inode = ICACHE.lock();

    // Is the inode already cached?
    let mut empty: *mut Inode = ptr::null_mut();
    for ip in &mut inode.deref_mut()[..] {
        if (*ip).ref_0 > 0 && (*ip).dev == dev && (*ip).inum == inum {
            (*ip).ref_0 += 1;
            return ip;
        }
        if empty.is_null() && (*ip).ref_0 == 0 {
            // Remember empty slot.
            empty = ip
        }
    }

    // Recycle an inode cache entry.
    assert!(!empty.is_null(), "iget: no inodes");
    let ip = empty;
    (*ip).dev = dev;
    (*ip).inum = inum;
    (*ip).ref_0 = 1;
    (*ip).inner.get_mut_unchecked().valid = false;
    ip
}
